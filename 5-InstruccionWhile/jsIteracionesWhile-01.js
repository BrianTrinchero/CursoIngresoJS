function mostrar()
{
	var numeroRepetir

	numeroRepetir = 0

	while(numeroRepetir < 10)
	{
		numeroRepetir = numeroRepetir + 1;
		document.write(numeroRepetir + "<br>");
	}

}//FIN DE LA FUNCIÓN